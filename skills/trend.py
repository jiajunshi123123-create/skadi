"""趋势分析技能 - 识别时间序列的变化方向和模式"""
from .base import AnalysisSkill


class TrendSkill(AnalysisSkill):
    name = "趋势分析"
    description = "识别时间序列的上升/下降/平稳趋势，计算移动平均和环比变化率"
    keywords = ["趋势", "走势", "变化", "增长", "下降", "上升", "平稳", "波动", "走向", "日活", "DAU", "日活跃", "新增", "注册", "活跃度", "增速", "涨幅", "跌幅", "攀升", "下滑", "活跃", "新增趋势", "日活趋势"]
    category = "statistical"
    priority = 5

    @property
    def prompt_snippet(self) -> str:
        return """
## 📈 趋势分析方法

请按以下框架分析数据趋势：

### 1. 趋势方向判定
- 观察数据序列的总体走向：持续上升 / 持续下降 / 先升后降 / 先降后升 / 波动平稳
- 计算首末值的变化幅度和变化率
- 标注趋势转折点（如有）

### 2. 移动平均平滑
- 如果数据点≥7个，计算3日/7日移动平均，平滑短期波动
- 用移动平均线判断中期趋势方向

### 3. 环比变化序列
- 计算逐期环比变化率
- 标注变化率最大的区间及其可能原因
- 判断变化是加速还是减速

### 4. 周期性识别
- 检查是否存在周周期性（工作日vs周末模式）
- 检查是否存在月初/月末效应

### 5. 趋势预测（定性）
- 基于当前趋势方向，推断短期（未来1-3期）的可能走向
- 标注预测的不确定性因素
"""
