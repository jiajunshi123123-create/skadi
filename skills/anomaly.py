"""异常检测技能 - 识别数据中的离群点和异常模式"""
from typing import List

from .base import AnalysisSkill


class AnomalySkill(AnalysisSkill):
    name = "异常检测"
    description = "基于统计方法识别数据中的异常值、突变点和离群模式"
    keywords = ["异常", "突变", "剧增", "骤降", "暴涨", "暴跌", "异常值", "离群", "不正常", "跳水", "断崖", "不对", "奇怪", "异常波动", "掉了", "跌了", "崩了", "流失", "数据风险"]
    category = "statistical"
    priority = 4

    @property
    def prompt_snippet(self) -> str:
        return """
## 🔴 异常检测方法

请按以下框架进行异常分析：

### 1. Z-Score 标准化检测
- 计算数据序列的均值和标准差
- 标注 |Z-Score| > 2 的数据点为"轻度异常"
- 标注 |Z-Score| > 3 的数据点为"显著异常"

### 2. 环比突变检测
- 计算相邻两期的环比变化率
- 变化率 > 50%：严重突变，需重点分析
- 变化率 20%-50%：显著变化
- 变化率 5%-20%：轻度波动

### 3. IQR 箱线图法
- 计算 Q1(25分位)、Q3(75分位)、IQR
- 标注 < Q1 - 1.5×IQR 或 > Q3 + 1.5×IQR 的离群点

### 4. 异常原因排查框架
对每个检测到的异常，按以下优先级分析原因：
1. 数据本身问题：数据延迟入库、埋点异常、口径变更
2. 业务事件驱动：活动/促销、版本更新、外部事件
3. 自然波动：节假日、季节性、周期性

### 5. 异常严重程度评级
- 🟢 轻度：在正常波动范围内，无需特别关注
- 🟡 中度：偏离基线20%-50%，建议关注
- 🔴 严重：偏离基线>50%，建议立即排查
"""
