"""对比分析技能 - 环比/同比/分组对比分析"""
from .base import AnalysisSkill


class ComparisonSkill(AnalysisSkill):
    name = "对比分析"
    description = "执行环比、同比、分组对比等多维度比较分析"
    keywords = ["对比", "比较", "环比", "同比", "相比", "差距", "差异", "昨天", "上周", "上月", "去年", "周同比", "日环比", "月环比", "变化了多少", "最近7天", "最近30天"]
    category = "statistical"
    priority = 5

    @property
    def prompt_snippet(self) -> str:
        return """
## 📊 对比分析方法

请按以下框架进行对比分析：

### 1. 确定对比维度
- 时间对比：环比（与上一期）、同比（与去年同期）、定基比（与固定基期）
- 分组对比：按类别/渠道/区域/产品等维度分组比较
- 目标对比：实际值 vs 目标值/基线值

### 2. 量化差异
- 绝对差异：A - B
- 相对差异：(A - B) / B × 100%（增长率/下降率）
- 贡献度：当存在多个分组时，计算各组对总体变化的贡献占比

### 3. 差异显著性判断
- 差异 < 5%：无显著差异
- 差异 5%-20%：存在可辨识差异
- 差异 20%-50%：显著差异，需分析原因
- 差异 > 50%：巨大差异，需重点关注

### 4. 帕累托分析（二八法则）
- 当存在多个对比组时，按贡献度降序排列
- 计算累计贡献度
- 标注贡献前80%的"关键少数"
- 标注贡献后20%的"长尾多数"

### 5. 对比结论
- 明确指出谁领先、谁落后，差距多大
- 说明差距是扩大还是缩小
- 给出资源分配的优先级建议
"""
