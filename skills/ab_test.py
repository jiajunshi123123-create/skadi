"""A/B测试分析技能 - 实验组/对照组对比、显著性检验、置信区间"""
from .base import AnalysisSkill


class ABTestSkill(AnalysisSkill):
    name = "A/B测试分析"
    description = "科学的A/B实验评估：对照组vs实验组对比、假设检验、置信区间计算"
    keywords = ["AB测试", "A/B", "实验", "对照组", "实验组", "测试组", "灰度", "放量", "abtest", "AB实验", "分流", "实验对比"]
    category = "testing"
    priority = 4

    @property
    def prompt_snippet(self) -> str:
        return """
## 🧪 A/B测试分析方法

请按以下框架进行A/B实验评估：

### 1. 实验设计确认
- 明确实验组和对照组的定义
- 确认样本量是否充分（每组至少1000样本才有统计效力）
- 检查是否存在SRM（样本比例不均，偏离50:50超过±2%）

### 2. 指标对比
- 计算两组的均值差异
- 计算相对提升/下降：(实验组 - 对照组) / 对照组 × 100%
- 同时关注北极星指标（核心指标）和护栏指标（不能恶化的指标）

### 3. 统计显著性检验
- T检验（两独立样本）：判断差异是否统计显著
  - p-value < 0.05：统计显著
  - p-value < 0.01：高度显著
  - p-value ≥ 0.05：不显著，差异可能由随机波动导致
- 说明：如果数据中给出了样本量和标准差，可以推断显著性

### 4. 置信区间
- 计算提升值的95%置信区间
- 置信区间不包含0 → 效果显著
- 置信区间包含0 → 效果不显著
- 置信区间较宽 → 样本量不足，建议继续积累数据

### 5. 实验结论与建议
- 明确建议：全量发布 / 继续实验 / 放弃该方案
- 给出决策的置信度
- 标注潜在风险（如：个别指标下降）
- 建议后续观察周期
"""
