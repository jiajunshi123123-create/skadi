"""关联分析技能 - 变量间相关性、交叉分析、同现模式"""
from .base import AnalysisSkill


class CorrelationSkill(AnalysisSkill):
    name = "关联分析"
    description = "探索变量间的关联关系和交叉影响，识别同现模式和因果线索"
    keywords = ["关联", "相关", "关系", "交叉", "同现", "伴随", "联动", "影响", "相关系数", "转化率", "留存率", "活跃率"]
    category = "statistical"
    priority = 2

    @property
    def prompt_snippet(self) -> str:
        return """
## 🔗 关联分析方法

请按以下框架探索变量间关联关系：

### 1. 相关系数矩阵
- 皮尔逊相关系数 r：衡量线性相关强度
  - |r| > 0.7：强相关
  - 0.4 < |r| ≤ 0.7：中等相关
  - 0.2 < |r| ≤ 0.4：弱相关
  - |r| ≤ 0.2：可忽略
  - r > 0：正相关（同向变化）
  - r < 0：负相关（反向变化）
- 注意：相关 ≠ 因果

### 2. 交叉分析（Cross-tab）
- 当存在分类变量时，构建交叉表
- 计算行列百分比，识别模式
- 关注偏离预期的格子（实际值 vs 期望值）

### 3. 时序联动分析
- 当数据包含时间维度时，对比不同指标的时序曲线
- 识别领先/滞后关系：A变化后，B是否随后变化？
- 关注同涨同跌的"同步指标"

### 4. 因果推断线索
- 时间先后：因在前，果在后
- 剂量反应：X的量变化 → Y的量相应变化
- 排除干扰：是否存在第三个变量Z同时影响X和Y？
- 机制合理：能否用业务逻辑解释？

### 5. 关联发现总结
- 列出最显著的关联关系（TOP3）
- 用业务语言解读关联含义
- 提出可验证的因果假设
- 建议下一步的验证方法（如A/B测试）
"""
