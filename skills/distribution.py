"""分布分析技能 - 数据分布形态、集中度、离散度分析"""
from .base import AnalysisSkill


class DistributionSkill(AnalysisSkill):
    name = "分布分析"
    description = "分析数据的分布形态（正态/偏态）、集中度和离散程度"
    keywords = ["分布", "集中", "分散", "分位数", "中位数", "均值", "长尾", "头部的", "占比", "分省", "分地域", "分年级", "分科目", "分组", "结构", "构成", "比例", "排行榜", "分级"]
    category = "statistical"
    priority = 3

    @property
    def prompt_snippet(self) -> str:
        return """
## 📐 分布分析方法

请按以下框架分析数据分布特征：

### 1. 集中趋势度量
- 均值（Mean）：数据的平均水平
- 中位数（Median）：数据的中心位置（抗异常值）
- 众数（Mode）：出现频率最高的值
- 当 均值 > 中位数 → 右偏分布（存在高端拉动）
- 当 均值 < 中位数 → 左偏分布（存在低端拖累）

### 2. 离散程度度量
- 极差（Range）= Max - Min
- 四分位距（IQR）= Q3 - Q1
- 标准差（Std）：数据波动程度
- 变异系数（CV）= Std / Mean：不同量纲间的可比离散度
  - CV < 0.15：低离散（数据集中）
  - CV 0.15-0.3：中等离散
  - CV > 0.3：高离散（数据分散，个体差异大）

### 3. 分位数分层
- 将数据按 P25 / P50 / P75 / P90 / P95 / P99 分层
- 分析头部集中度：TOP 10% 贡献了多少？
- 识别是否存在"超级头部"（P99 >> P95）

### 4. 长尾效应分析
- 判断数据是否符合帕累托分布（20%头部贡献80%总量）
- 计算基尼系数（如适用）：0=完全均等, 1=完全不均等

### 5. 分布结论
- 描述分布的形态特征（正态/偏态/双峰/均匀）
- 指出集中与分散的关键发现
- 对业务决策的启示（如：是否需要差异化策略）
"""
