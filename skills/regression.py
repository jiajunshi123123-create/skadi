"""回归分析技能 - 线性回归、多元回归、变量关系建模"""
from .base import AnalysisSkill


class RegressionSkill(AnalysisSkill):
    name = "回归分析"
    description = "建立变量间的量化关系模型，识别关键驱动因素，预测因变量变化"
    keywords = ["回归", "相关性", "影响因子", "驱动", "决定因素", "预测模型", "线性关系", "因素", "自变量", "因变量", "影响因素", "什么影响", "原因分析", "归因"]
    category = "ml"
    priority = 3

    @property
    def prompt_snippet(self) -> str:
        return """
## 📈 回归分析方法

请按以下框架进行变量关系分析：

### 1. 变量识别
- 明确因变量（Y，要预测/解释的指标）
- 列出自变量（X1, X2, ...，可能的影响因素）
- 判断变量类型：连续型 / 分类型

### 2. 散点图关系初判
- 逐一观察 Y vs 每个 X 的数据分布
- 判断关系类型：线性 / 非线性 / 无明显关系
- 识别异常点（可能影响回归结果）

### 3. 线性回归模型（如适用）
- 回归方程：Y = β₀ + β₁X₁ + β₂X₂ + ...
- 解读 β系数：X每变化1个单位，Y的平均变化量
- R²（拟合优度）：模型解释了多少变异
  - R² > 0.7：拟合良好
  - R² 0.4-0.7：中等拟合
  - R² < 0.4：拟合较弱，可能存在遗漏变量

### 4. 变量重要性排序
- 标准化系数比较：消除量纲后比较各变量的相对重要性
- 识别TOP3驱动因素
- 标注统计不显著的变量（p > 0.05）

### 5. 业务解读
- 将统计结论转换为业务语言
- 例如："每增加1%营销预算，预期带来0.3%的收入增长"
- 标注模型的局限性和适用范围
- 给出优化建议：优先提升哪个变量效果最大
"""
