"""时间序列预测技能 - 基于历史数据预测未来趋势"""
from .base import AnalysisSkill


class TimeSeriesSkill(AnalysisSkill):
    name = "时间序列预测"
    description = "基于历史时序数据，进行短期趋势预测和季节性分解"
    keywords = ["预测", "预估", "预计", "未来", "推算", "走向", "预期", "会怎样", "推测", "接下来", "下周", "下个月"]
    category = "ml"
    priority = 3

    @property
    def prompt_snippet(self) -> str:
        return """
## 🔮 时间序列预测方法

请按以下框架进行时序分析与预测：

### 1. 时序分解
将时间序列分解为三个组分：
- **趋势项（Trend）**：长期方向性变化
- **季节项（Seasonal）**：固定周期的重复模式（如周末效应、月度周期）
- **残差项（Residual）**：随机波动

### 2. 预测方法选择
根据数据特点选择合适方法：
- **简单移动平均**：适合无趋势/无季节的平稳序列，取最近N期均值
- **加权移动平均**：近期数据赋予更高权重
- **线性外推**：适合有明显线性趋势的数据，拟合Y=aX+b后外推
- **同比参照**：适合有强季节性的数据，用去年同期值作为预测

### 3. 短期预测（1-3期）
- 给出点预测值
- 给出预测区间（±1个标准差）
- 标注预测假设条件

### 4. 预测准确度评估
- 如果数据足够，用留出法验证（用前N-1期预测第N期）
- 计算MAPE（平均绝对百分比误差）
  - MAPE < 10%：预测效果良好
  - MAPE 10%-20%：可接受
  - MAPE > 20%：预测精度较低，建议仅作参考

### 5. 预测结论
- 给出明确的预测数值和方向
- 标注预测的风险因素和不确定性
- 建议：何时需要修正预测（触发条件）
"""
